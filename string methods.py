#cool things we can do with python strings
course = 'Python for Beginners'

(len(course))                       #len digunakan untuk menghitung jumlah karakter
(course.upper())                    #.upper digunakan untuk membuat huruf besar semuanya
(course.lower())                    #.lower digunakan untuk mengecilkan huruf
(course.find('P'))                  #.find digunakan untuk mencari karakter yang kita inginkan
(course.replace('P', 'J'))          #.replace digunakan untuk mengganti karakter
(course.title())
'...' in course                     #in digunakan untuk menentukan ada atau tidak karakter di variabel. menghasilkan Boolean

#Remember, string karakter is very sensitive. akan berbeda hasilnya jika tidak sama dengan yang asli.